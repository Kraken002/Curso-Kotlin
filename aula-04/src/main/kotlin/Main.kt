fun main(args: Array<String>) {
 /*
   + soma
    - subtração
    / divisão
    * multiplicação
    % modulo - resto da divisão
    -- decremento
    ++ incremento
  */
   var numero1 = 20
    var numero2 = 10

    println(numero1 + numero2)
    println (numero1 - numero2)
    println (numero1 / numero2)
    println (numero1 * numero2)
    println (numero1 % numero2)
    println (++ numero1  )
    println (-- numero2  )
}